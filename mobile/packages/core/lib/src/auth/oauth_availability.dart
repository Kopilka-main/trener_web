import 'dart:io';

/// Показывать ли кнопки соц-входа (VK/Яндекс) в приложении.
///
/// На iOS скрыты: Apple Guideline 4.8 требует «Вход через Apple» при наличии
/// стороннего соц-логина. До его добавления VK/Яндекс доступны только на
/// Android и в веб-версии. Чтобы вернуть их на iOS — снять условие здесь
/// (единственное место, от которого зависят все экраны входа/регистрации).
bool get oauthLoginEnabled => !Platform.isIOS;
